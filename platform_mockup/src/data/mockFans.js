// Helper function to get fans by status
export const getFansByStatus = (status) => mockFans.filter(fan => fan.status === status)

// Helper function to get top fans by LTV
export const getTopFansByLTV = (count = 5) => 
  [...mockFans].sort((a, b) => b.ltv - a.ltv).slice(0, count)

// Helper function to get at-risk fans
export const getAtRiskFans = () => mockFans.filter(fan => fan.churnRisk === 'High' || fan.churnRisk === 'Medium')

// Helper function to get fans needing attention
export const getFansNeedingAttention = () => mockFans.filter(fan => 
  fan.healthTrend === 'down' || fan.churnRisk !== 'Low' || fan.lastInteractionDays > 5
)

// Get a single fan by ID
export const getFanById = (id) => mockFans.find(fan => fan.id === id)

